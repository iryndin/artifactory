public class FileExpectedException extends Exception {
    private final RepoPath repoPath;
    private static final String MESSGAE_PREFIX = "Expected a file but found a folder, at: ";

    public FileExpectedException(RepoPath repoPath) {
        super(MESSGAE_PREFIX + repoPath);
        this.repoPath = repoPath;
    }

    public RepoPath getRepoPath() {
        return repoPath;
    }
}