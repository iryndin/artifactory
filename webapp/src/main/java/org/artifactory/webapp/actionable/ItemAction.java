package org.artifactory.webapp.actionable;

import org.apache.log4j.Logger;

import javax.swing.AbstractAction;
import java.awt.event.ActionEvent;

/**
 * Created by IntelliJ IDEA. User: yoav
 */
public abstract class ItemAction extends AbstractAction {
    @SuppressWarnings({"UNUSED_SYMBOL", "UnusedDeclaration"})
    private final static Logger LOGGER = Logger.getLogger(ItemAction.class);

    public ItemAction(String name) {
        super(name);
    }

    public void actionPerformed(ActionEvent e) {
        actionPerformed((ItemActionEvent) e);
    }

    public abstract void actionPerformed(ItemActionEvent e);
}
